function getTpe(tpeId) {
    // Walk through dimensions structure, looking for a tpe item with the specified ID.

    const dimensionsKeys = Object.getOwnPropertyNames(DATA);
    for (let i = 0; i < dimensionsKeys.length; ++i) {
        const objectives = DATA[dimensionsKeys[i]].objectives;
        const objectivesKeys = Object.getOwnPropertyNames(objectives)
        for (let j = 0; j < objectivesKeys.length; ++j) {
            const tpe = objectives[objectivesKeys[j]].tpe;
            for (let k = 0; k < tpe.length; ++k) {
                const tpeItem = tpe[k];
                if (tpeItem.id === tpeId) {
                    return tpeItem.description;
                }
            }
        }
    }
    return "No data"; // If we got this far, we couldn't find the tpe ID.
}
  
exports.DATA = DATA;
exports.getTpe = getTpe;